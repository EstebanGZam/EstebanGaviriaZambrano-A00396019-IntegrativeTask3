package model;

public class PlaybackHistory {
    int rock = 0, pop = 0, trap = 0, house = 0, politic = 0, entertainment = 0, videoGame = 0, fashion = 0;

    public PlaybackHistory() {
    }

    
    /** 
     * @return int
     */
    public int getRock() {
        return rock;
    }

    
    /** 
     * @param rock
     */
    public void setRock(int rock) {
        this.rock = rock;
    }

    
    /** 
     * @return int
     */
    public int getPop() {
        return pop;
    }

    
    /** 
     * @param pop
     */
    public void setPop(int pop) {
        this.pop = pop;
    }

    
    /** 
     * @return int
     */
    public int getTrap() {
        return trap;
    }

    
    /** 
     * @param trap
     */
    public void setTrap(int trap) {
        this.trap = trap;
    }

    
    /** 
     * @return int
     */
    public int getHouse() {
        return house;
    }

    
    /** 
     * @param house
     */
    public void setHouse(int house) {
        this.house = house;
    }

    
    /** 
     * @return int
     */
    public int getPolitic() {
        return politic;
    }

    
    /** 
     * @param politic
     */
    public void setPolitic(int politic) {
        this.politic = politic;
    }

    
    /** 
     * @return int
     */
    public int getEntertainment() {
        return entertainment;
    }

    
    /** 
     * @param entertainment
     */
    public void setEntertainment(int entertainment) {
        this.entertainment = entertainment;
    }

    
    /** 
     * @return int
     */
    public int getVideoGame() {
        return videoGame;
    }

    
    /** 
     * @param videoGame
     */
    public void setVideoGame(int videoGame) {
        this.videoGame = videoGame;
    }

    
    /** 
     * @return int
     */
    public int getFashion() {
        return fashion;
    }

    
    /** 
     * @param fashion
     */
    public void setFashion(int fashion) {
        this.fashion = fashion;
    }
}
